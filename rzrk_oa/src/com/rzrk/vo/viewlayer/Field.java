package com.rzrk.vo.viewlayer;

public class Field {
	public static final int TYPE_TABLE=0;
	public static final int TYPE_EXPRESSION=1;
	
	int type;
	String tableId;
	String tableName;
	String tableField;
	String showField;
	String expression;
	String total;
	
	
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public String getExpression() {
		return expression;
	}
	public void setExpression(String expression) {
		this.expression = expression;
	}
	public String getTableId() {
		return tableId;
	}
	public void setTableId(String tableId) {
		this.tableId = tableId;
	}
	public String getTableName() {
		return tableName;
	}
	public void setTableName(String tableName) {
		this.tableName = tableName;
	}
	public String getTableField() {
		return tableField;
	}
	public void setTableField(String tableField) {
		this.tableField = tableField;
	}
	public String getShowField() {
		return showField;
	}
	public void setShowField(String showField) {
		this.showField = showField;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	@Override
	public String toString() {
		return "Field [type=" + type + ", tableId=" + tableId + ", tableName="
				+ tableName + ", tableField=" + tableField + ", showField="
				+ showField + ", expression=" + expression + ", total=" + total
				+ "]";
	}
	
}
