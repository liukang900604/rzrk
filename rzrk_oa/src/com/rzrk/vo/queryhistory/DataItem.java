package com.rzrk.vo.queryhistory;

public class DataItem {
	/**
	 * 根菜单id
	 */
	private String rcId;
	/**
	 * 根菜单名称
	 */
	private String rcName;
	/**
	 * 二级分类id
	 */
	private String ccId;
	/**
	 * 二级分类名称
	 */
	private String ccName;
	/**
	 * 查询记录id
	 */
	private String qhId;
	/**
	 * 查询名称
	 */
	private String qhName;
	/**
	 * 查询内容
	 */
	private String qhContent;
	/**
	 * 查询类型
	 */
	private int qhType;
	
	public String getRcId() {
		return rcId;
	}
	public void setRcId(String rcId) {
		this.rcId = rcId;
	}
	public String getRcName() {
		return rcName;
	}
	public void setRcName(String rcName) {
		this.rcName = rcName;
	}
	public String getCcId() {
		return ccId;
	}
	public void setCcId(String ccId) {
		this.ccId = ccId;
	}
	public String getCcName() {
		return ccName;
	}
	public void setCcName(String ccName) {
		this.ccName = ccName;
	}
	public String getQhId() {
		return qhId;
	}
	public void setQhId(String qhId) {
		this.qhId = qhId;
	}
	public String getQhName() {
		return qhName;
	}
	public void setQhName(String qhName) {
		this.qhName = qhName;
	}
	
	public String getQhContent() {
		return qhContent;
	}
	public void setQhContent(String qhContent) {
		this.qhContent = qhContent;
	}
	@Override
	public String toString() {
		return "DataItem [rcId=" + rcId + ", rcName=" + rcName + ", ccId="
				+ ccId + ", ccName=" + ccName + ", qhId=" + qhId + ", qhName="
				+ qhName + ", qhContent=" + qhContent + "]";
	}
	
	
}
